# Atribuindo um valor à variável texto
texto = 'Olá, laço for.'

# Iterando sobre cada caractere da string
for item in texto:
    # Imprimindo o caractere precedido do texto
    print('Caractere: ' + item)

# Iterando sobre o intervalo de 1 a 10
for numero in range(1, 11):
    # Imprimindo o número precedido do texto
    print('Número do intervalo: ' + str(numero))